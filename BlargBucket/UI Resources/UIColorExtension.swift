import UIKit

extension UIColor {
	// cobalt
	class func sharkFinGray() -> UIColor {
		return UIColor(red: 51/255.0, green: 117/255.0, blue: 156/255.0, alpha: 1.0)
	}
	// mintJulip
	class func seafoam() -> UIColor {
		return UIColor(red: 125/255.0, green: 184/255.0, blue: 180/255.0, alpha: 1.0)
	}

	class func yellowish() -> UIColor {
		return UIColor(red: 222/255.0, green: 217/255.0, blue: 89/255.0, alpha: 1.0)
	}

	class func orangeCream() -> UIColor {
		return UIColor(red: 229/255.0, green: 136/255.0, blue: 37/255.0, alpha: 1.0)
	}
	// lavendar
	class func badicalRed() -> UIColor {
		return UIColor(red: 160/255.0, green: 102/255.0, blue: 189/255.0, alpha: 1.0)
	}

}
